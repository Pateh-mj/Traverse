  /// Status colors for fleet operations
  static Color getStatusColor(String status) {
    switch (status.toLowerCase()) {
      case 'online':
      case 'active':
      case 'available':
        return onlineStatus;
      case 'offline':
      case 'inactive':
      case 'unavailable':
        return offlineStatus;
      case 'alert':
      case 'critical':
      case 'emergency':
        return alertStatus;
      case 'warning':
      case 'caution':
        return warningStatus;
      default:
        return secondaryLight;
    }
  }
